void draw_element(const char** land) {

  int rows = 0;
  // Get the number of rows in the array
  while (land[rows] != NULL) {
    rows++;
  }

  // Draw the game field
  for (int i = 0; i < rows; i++) {
    for (int y = 0, len = strlen(land[i]); y < len; y++) {
      printf("%c", land[i][y]);
    }
    printf("\n");
  }
}

